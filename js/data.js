// LocalStorage key prefix
window.KKMA_PREFIX = 'kkma04_';

// Load/Save helpers
window.kkmaLoad = function(key) {
    try {
        const raw = localStorage.getItem(KKMA_PREFIX + key);
        return raw ? JSON.parse(raw) : null;
    } catch(e) { return null; }
};

window.kkmaSave = function(key, data) {
    localStorage.setItem(KKMA_PREFIX + key, JSON.stringify(data));
};

// Initialize data from localStorage or defaults
(function initData() {
    const savedMadrasah = kkmaLoad('madrasah');
    if (savedMadrasah) KKMA_DATA.madrasah = savedMadrasah;

    const savedGuru = kkmaLoad('guru');
    if (savedGuru) KKMA_DATA.guru = savedGuru;

    const savedSiswa = kkmaLoad('siswa');
    if (savedSiswa) KKMA_DATA.siswa = savedSiswa;
})();
